          <p>But NASA’s two other Mars orbiters have been in space for more than 20 years. The older of the two, <a
              href="https://science.nasa.gov/mission/odyssey/">named Mars Odyssey</a>, has been at Mars since 2001 and
            will soon run out of fuel, probably some time in the next couple of years. NASA’s <a
              href="https://science.nasa.gov/mission/mars-reconnaissance-orbiter/">Mars Reconnaissance Orbiter</a>,
            which launched in 2005, is healthy for its age, with enough fuel to last into the 2030s. MRO is also
            important to NASA because it has the best camera at Mars, with the ability to map landing sites for future
            missions.</p>
          <p>Two European spacecraft, Mars Express and the ExoMars Trace Gas Orbiter, have radios to relay data between
            mission controllers and NASA’s landers on the Martian surface. Mars Express, now 22 years old, suffers from
            the same aging concerns as Mars Odyssey and MRO. The ExoMars Trace Gas Orbiter is newer, having arrived at
            Mars in 2016, but is also operating beyond its original lifetime.</p>
          <p>China and the United Arab Emirates also have orbiters circling Mars, but neither spacecraft is equipped to
            serve as a communications relay.</p>
          <p>NASA’s Curiosity and Perseverance rovers have the capability for direct-to-Earth communications, but the
            orbiting relay network can support vastly higher data throughput. Without overhead satellites, much of the
            science data and many of the spectacular images collected by NASA’s rovers might never make it off the
            planet.</p>
          <p>MAVEN’s unique orbit, stretching as far as 2,800 miles (4,500 kilometers) above Mars, has some advantages
            for data relay. In that orbit, MAVEN could relay science data from rovers on the surface for up to 30
            minutes at a time, longer than the relay periods available through NASA’s lower-altitude orbiters. Because
            of this, MAVEN could support the largest data volumes of any of the other relay options.</p>


