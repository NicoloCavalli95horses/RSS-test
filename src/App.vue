          <p>Before the loss of communications last weekend, NASA said MAVEN had sufficient fuel reserves to operate
            until at least the late 2030s.</p>
          <p>MAVEN was <a
              href="https://arstechnica.com/space/2025/10/one-nasa-science-mission-saved-from-trumps-cuts-but-others-still-in-limbo/">one
              of 19 operating missions</a> targeted for cancellation in President Donald Trump’s proposed NASA budget
            for fiscal year 2026. Congress largely rejected the Trump’s administration’s proposed budget cuts at NASA.
          </p>
          <p>Mars is clearly a second priority at NASA amid a <a
              href="https://arstechnica.com/space/2025/12/congress-told-there-needs-to-be-consequences-for-nasa-delays-amid-chinas-rise/">race
              between the United States and China</a> to land astronauts on the Moon. But the recent trouble with MAVEN
            will likely rekindle <a href="https://www.thespacereview.com/article/3257/1">long-running concerns about
              NASA’s aging spacecraft</a> at Mars, especially the risk of losing capacity for communications relay and
            high-resolution mapping to support NASA’s active rovers, along with future robotic or human expeditions.</p>
          <p>The space agency has investigated using commercial relay services to replace the government-owned network
            currently in place at Mars. NASA <a
              href="https://www.nasa.gov/solar-system/planets/mars/nasa-selects-commercial-service-studies-to-enable-mars-robotic-science/">awarded
              study contracts</a> to Blue Origin, Lockheed Martin, and SpaceX in 2024 to examine possible data relay
            architectures.</p>
          <p>NASA was in the early stages of planning for a dedicated Mars Telecommunications Orbiter more than 20 years
            ago, with plans to put it into an orbit similar to the one occupied by MAVEN. The agency canceled the
            project in 2005. Since then, NASA has tacked on relay radios to science orbiters sent to Mars, giving them
            the ability to provide communications support as a secondary mission.</p>
          <p>The Mars Telecommunications Orbiter name appeared again in the “One Big Beautiful Bill” passed by Congress
            earlier this year. Lawmakers included $700 million for a “high-performance” telecom relay station in Mars
            orbit to be developed through a fixed-price contract. <a
              href="https://www.blueorigin.com/es-MX/news/blue-origin-mars-telecommunications-orbiter">Blue Origin</a>
            and <a href="https://rocketlabcorp.com/missions/mars-comms-orbiter/">Rocket Lab</a> are among the companies
            that have publicized their concepts for a Mars telecom orbiter in recent months. NASA officials have not
            said when they will release a request for bids for such a mission.</p>



